export const uiTranslations: Record<Locale, TranslationDictionary> = {
  vi,
  en,
};

export function resolveLocale(rawLocale: string | null | undefined): Locale {
  if (!rawLocale) {
    return "vi";
  }

  const normalized = rawLocale.toLowerCase();

  if (normalized === "en" || normalized.startsWith("en-")) {
    return "en";
  }

  return "vi";
}

export function translate(locale: Locale, key: string) {
  return uiTranslations[locale][key] ?? uiTranslations.en[key] ?? key;
}
